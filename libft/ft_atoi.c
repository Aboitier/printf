#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int integer;
	int	sign;

	i = 0;
	integer = 0;
	sign = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && ft_isdigit(str[i]) == 1)
	{
		integer = integer * 10 + (str[i] - 48);
		i++;
	}
	return (sign * integer);
}
